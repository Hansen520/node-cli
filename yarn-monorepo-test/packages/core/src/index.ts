function add(a: number, b: number) {
    return a + b;
}

function minus(a: number, b: number) {
    return a - b;
}

function divide(a: number, b: number) {
    return a / b;
}

function multiply(a: number, b: number) {
    return a * b;
}

function power(a: number, b: number) {
    return a ** b;
}

function radical(a: number) {
    return Math.sqrt(a);
}

export {
    add,
    minus,
    divide,
    multiply,
    power,
    radical
}
